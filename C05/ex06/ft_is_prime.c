#include <unistd.h>

int	ft_is_prime(int nb)
{
	int	y;

	if (nb <= 1)
		return (0);
	y = 2;
	while (y * y <= nb)
	{
		if (nb % y == 0)
			return (0);
		y++;
	}
	return (1);
}
