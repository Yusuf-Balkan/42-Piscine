#include <unistd.h>

int	ft_sqrt(int nb)
{
	int	y;

	if (nb <= 0)
		return (0);
	y = 1;
	while (y * y <= nb)
	{
		if (y * y == nb)
			return (y);
		y++;
	}
	return (0);
}
